import { readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const root = resolve(new URL('..', import.meta.url).pathname)
const productDir = resolve(root, 'docs/product')
const manifest = JSON.parse(readFileSync(resolve(productDir, 'manifest.json'), 'utf8'))

const docs = manifest.documents.map((entry) => {
  const lines = readFileSync(resolve(productDir, entry.file), 'utf8').trim().split('\n')
  const title = lines.find((line) => line.startsWith('# '))?.slice(2).trim()
  if (!title) throw new Error(`Missing title in ${entry.file}`)
  const blocks = []
  let current = null
  for (const line of lines) {
    if (line.startsWith('## ')) {
      if (current) blocks.push(current)
      current = { heading: line.slice(3).trim(), body: '', bullets: [] }
    } else if (current && line.startsWith('- ')) {
      current.bullets.push(line.slice(2).trim())
    } else if (current && line.trim()) {
      current.body = `${current.body}${current.body ? ' ' : ''}${line.trim()}`
    }
  }
  if (current) blocks.push(current)
  const summary = lines.filter((line) => !line.startsWith('#') && line.trim() && !line.startsWith('- '))[0] ?? ''
  return { id: entry.id, route: entry.route, title, status: entry.status, summary, blocks }
})

const output = `// Generated by scripts/build-product-docs.mjs. Do not edit manually.\n` +
  `import type { ProductDoc } from './types'\n\n` +
  `export const productDocs: ProductDoc[] = ${JSON.stringify(docs, null, 2)}\n`
const outputPath = resolve(root, 'src/product-docs/productDocs.ts')
if (process.argv.includes('--check')) {
  const current = readFileSync(outputPath, 'utf8')
  if (current !== output) throw new Error('Generated product docs are stale; run npm run docs:build')
  console.log(`Product docs snapshot current: ${docs.length} documents`)
} else {
  writeFileSync(outputPath, output)
  console.log(`Product docs snapshot generated: ${docs.length} documents`)
}
