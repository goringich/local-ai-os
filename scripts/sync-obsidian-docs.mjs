const dryRun = process.env.OBSIDIAN_SYNC_DRY_RUN === '1'
const copied = []

for (const target of targets) {
  for (const relative of productDocs) {
    const source = resolve(root, relative)
    const destination = resolve(target.dir, basename(relative))
    if (!dryRun) {
      mkdirSync(dirname(destination), { recursive: true })
      copyFileSync(source, destination)
    }
    copied.push(`${target.name}:${destination}`)
  }

  const indexPath = resolve(target.dir, 'README.md')
  const index = buildIndex(target.name)
  if (!dryRun) {
    mkdirSync(target.dir, { recursive: true })
    writeFileSync(indexPath, index)
  }
  copied.push(`${target.name}:${indexPath}`)
}

for (const note of canonicalNotes) {
  if (!dryRun) upsertSection(note.path, note.marker, note.body)
  copied.push(`canonical:${note.path}`)
}

console.log(`${dryRun ? 'Dry-run docs sync' : 'Docs synced'}\n${copied.join('\n')}`)

function buildIndex(targetName) {
  const docs = productDocs.map((path) => `- [[${basename(path, '.md')}]]`).join('\n')
  const release = safeRead(resolve(root, 'docs/release-status.md')).split('\n').slice(0, 10).join('\n')
  return `# LOCAL AI OS Product Documentation\n\n` +
    `Target: ${targetName}\n\n` +
    `This folder is generated by \`npm run docs:sync\` from \`/home/goringich/Desktop/local-ai-os/docs\`.\n` +
    `Edit source docs in the repository, then sync again.\n\n` +
    `## Documents\n\n${docs}\n\n` +
    `## Latest Release Snapshot\n\n${release}\n`
}

function safeRead(path) {
  try {
    return readFileSync(path, 'utf8')
  } catch {
    return ''
  }
}

function upsertSection(path, marker, body) {
  const source = safeRead(path)
  if (!source) return
  const escaped = marker.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  const nextHeading = '\\n### '
  const pattern = new RegExp(`${escaped}[\\s\\S]*?(?=${nextHeading}|\\n## |$)`)
  const next = pattern.test(source)
    ? source.replace(pattern, body)
    : source.replace('## Коротко\n', `## Коротко\n\n${body}\n`)
  writeFileSync(path, next.endsWith('\n') ? next : `${next}\n`)
}
